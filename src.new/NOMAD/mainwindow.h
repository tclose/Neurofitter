/* mainwindow.h -- class definition for MainWindow. It has public inheritance
   from the QMainWindow class. */

#ifndef MAINWINDOW_H
#define MAINWINDOW_H
#include <qmainwindow.h>

class MainApplication;

// MainWindow inherits publicly from QMainWindow.
class MainWindow : public QMainWindow
{
  /* Only the constructor and destructor methods are overwritten. They're
     described in "mainwindow.cpp". */
public:
  MainWindow(int argc, char * argv[]);
  ~MainWindow();

/* The MainWindow object has the following variables:
   - a pointer to an object of the MainApplication class, "main_app". */
private:
  // The "main_app" object fills the GUI's main window.
  MainApplication * main_app;
};
#endif
